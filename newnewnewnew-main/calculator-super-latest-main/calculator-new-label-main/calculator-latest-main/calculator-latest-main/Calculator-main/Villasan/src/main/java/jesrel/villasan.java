     private void performOperation(String operator, double secondNum) {
        switch (operator) {
            case "+":
                ans = num1 +  secondNum;
                break;
            case "-":
                ans = num1 - secondNum;
                break;
            case "*":
                ans = num1 * secondNum;
                break;
            case "%":
                ans = num1 % secondNum;
                break;
            case "/":
                if (secondNum != 0) {
                    ans = num1 / secondNum;
                } else {
                    ans = Double.POSITIVE_INFINITY;
                }
                break;
        }
       }
