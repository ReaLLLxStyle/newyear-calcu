    private void jbtnEqualsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnEqualsActionPerformed
    if (!currentNumber.isEmpty() && !operator.isEmpty()) {
            double secondNum = Double.parseDouble(jlblOutput.getText());
            performOperation(operator, secondNum);
            jlblOutput.setText("");
                if (ans % 1 == 0) {
                    jlblOutput.setText(String.format(""+"%.0f", ans));
                } else {   
                    jlblOutput.setText(""+Double.toString(ans));
                }
            currentNumber = String.valueOf(ans);
            operator = "";
        } else {
            jlblOutput.setText(jlblOutput.getText());
        }
    
    }//GEN-LAST:event_jbtnEqualsActionPerformed

